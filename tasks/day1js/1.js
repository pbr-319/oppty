// Arithmetric Operators
let num1 = 15;
let num2 = 5;

console.log("Addition:", num1 + num2);
console.log("Subtraction:", num1 - num2);
console.log("Multiplication:", num1 * num2);
console.log("Division:", num1 / num2);
console.log("Modulus:", num1 % num2);

// Assignment Operators
let a = 5;
let b = 3;

a + b;
console.log("a += b:",a);

a - b;
console.log("a -= b:",a);

a * b;
console.log("a *= b:",a);

a / b;
console.log("a /= b:",a);

a % b;
console.log("a %= b:",a);

// Comparison Operators
let x = 2;
let y = 3;

console.log("A:", x == y);
console.log("B:", x != y);
console.log("C:", x === y);
console.log( "D:",x !== y);
console.log("E:", x > y);
console.log("F:", x >= y);
console.log("G:", x < y);
console.log("H", x <= y);

// Logical Operators
let age = 20;
let ID = true;

console.log("I:", age >= 18 && ID);
console.log("J:", age < 18 || ID);
console.log("K:", !ID);




